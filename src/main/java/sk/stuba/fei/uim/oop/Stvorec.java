package sk.stuba.fei.uim.oop;

public class Stvorec {
    private boolean horna;
    private boolean dolna;
    private boolean prava;
    private boolean lava;


    public boolean isHorna() {
        return horna;
    }

    public boolean isDolna() {
        return dolna;
    }

    public boolean isPrava() {
        return prava;
    }

    public boolean isLava() {
        return lava;
    }

    public void setHorna(boolean horna) {
        this.horna = horna;
    }

    public void setDolna(boolean dolna) {
        this.dolna = dolna;
    }

    public void setPrava(boolean prava) {
        this.prava = prava;
    }

    public void setLava(boolean lava) {
        this.lava = lava;
    }
}
